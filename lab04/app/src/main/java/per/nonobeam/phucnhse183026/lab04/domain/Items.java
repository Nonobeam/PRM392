package per.nonobeam.phucnhse183026.lab04.domain;

public class Items {
    int id;
    String name;

    public Items(int id, String name) {
        this.id = id;
        this.name = name;
    }

    public int getId() {
        return id;
    }

    public void setId(int id) {
        this.id = id;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }
}
